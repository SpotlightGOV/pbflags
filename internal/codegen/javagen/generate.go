func extractFeatureOptions(msg *protogen.Message) *featureInfo {
	opts := msg.Desc.Options()
	if opts == nil {
		return nil
	}
	protoMsg := opts.(interface{ ProtoReflect() protoreflect.Message })
	rm := protoMsg.ProtoReflect()

	var feat *featureInfo
	rm.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		if fd.Number() == 51000 && fd.IsExtension() {
			m := v.Message()
			info := &featureInfo{}
			m.Range(func(innerFd protoreflect.FieldDescriptor, innerV protoreflect.Value) bool {
				switch innerFd.Name() {
				case "id":
					info.id = innerV.String()
				case "description":
					info.description = innerV.String()
				case "owner":
					info.owner = innerV.String()
				case "scopes":
					list := innerV.List()
					for i := 0; i < list.Len(); i++ {
						info.scopes = append(info.scopes, list.Get(i).String())
					}
				}
				return true
			})
			if info.id != "" {
				feat = info
			}
			return false
		}
		return true
	})
	if feat != nil {
		return feat
	}

	unk := rm.GetUnknown()
	if len(unk) == 0 {
		return nil
	}
	return parseFeatureFromUnknown(unk)
}

func parseFeatureFromUnknown(b []byte) *featureInfo {
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		if num == 51000 && typ == protowire.BytesType {
			data, n := protowire.ConsumeBytes(b)
			if n < 0 {
				return nil
			}
			return parseFeatureMessage(data)
		}
		n = skipField(b, typ)
		if n < 0 {
			return nil
		}
		b = b[n:]
	}
	return nil
}

func parseFeatureMessage(b []byte) *featureInfo {
	info := &featureInfo{}
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		if typ != protowire.BytesType {
			n = skipField(b, typ)
			if n < 0 {
				return nil
			}
			b = b[n:]
			continue
		}
		data, n := protowire.ConsumeBytes(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		switch num {
		case 1:
			info.id = string(data)
		case 2:
			info.description = string(data)
		case 3:
			info.owner = string(data)
		case 4: // repeated string scopes
			info.scopes = append(info.scopes, string(data))
		}
	}
	if info.id == "" {
		return nil
	}
	return info
}

func extractFlags(msg *protogen.Message) ([]flagInfo, error) {
	var flags []flagInfo
	for _, field := range msg.Fields {
		fl, err := extractFlagFromField(field)
		if err != nil {
			return nil, err
		}
		if fl != nil {
			flags = append(flags, *fl)
		}
	}
	return flags, nil
}

func extractFlagFromField(field *protogen.Field) (*flagInfo, error) {
	opts := field.Desc.Options()
	if opts == nil {
		return nil, nil
	}
	protoMsg := opts.(interface{ ProtoReflect() protoreflect.Message })
	rm := protoMsg.ProtoReflect()

	var found bool
	var defaultVal string
	var hasDefault bool
	var description string

	rm.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		if fd.Number() == 51001 && fd.IsExtension() {
			found = true
			m := v.Message()
			m.Range(func(innerFd protoreflect.FieldDescriptor, innerV protoreflect.Value) bool {
				switch innerFd.Name() {
				case "default":
					defaultVal, hasDefault = extractDefaultReflect(innerV.Message(), field.Desc.Kind())
				case "description":
					description = innerV.String()
				}
				return true
			})
			return false
		}
		return true
	})

	if !found {
		unk := rm.GetUnknown()
		if len(unk) == 0 {
			return nil, nil
		}
		found, defaultVal, hasDefault, description = parseFlagFromUnknown(unk, field.Desc.Kind())
		if !found {
			return nil, nil
		}
	}

	fieldName := string(field.Desc.Name())
	isList := field.Desc.IsList()
	javaType, javaBoxedType, javaClassLiteral := javaTypeInfo(field.Desc.Kind(), isList)

	return &flagInfo{
		camelName:        toCamelCase(fieldName),
		constName:        toScreamingSnake(fieldName),
		fieldNumber:      int32(field.Desc.Number()),
		javaType:         javaType,
		javaBoxedType:    javaBoxedType,
		javaClassLiteral: javaClassLiteral,
		defaultVal:       defaultVal,
		hasDefault:       hasDefault,
		isList:           isList,
		description:      description,
	}, nil
}

func extractDefaultReflect(defMsg protoreflect.Message, fieldKind protoreflect.Kind) (string, bool) {
	var val string
	var ok bool
	defMsg.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		switch fd.Name() {
		case "bool_list_value", "string_list_value", "int64_list_value", "double_list_value":
			val, ok = extractListDefaultReflect(fd.Name(), v.Message())
			return false
		}
		wrapperMsg := v.Message()
		wrapperMsg.Range(func(wfd protoreflect.FieldDescriptor, wv protoreflect.Value) bool {
			if wfd.Name() != "value" {
				return true
			}
			switch fd.Name() {
			case "bool_value":
				ok = true
				if wv.Bool() {
					val = "true"
				} else {
					val = "false"
				}
			case "string_value":
				ok = true
				val = fmt.Sprintf("%q", wv.String())
			case "int64_value":
				ok = true
				val = fmt.Sprintf("%dL", wv.Int())
			case "double_value":
				ok = true
				val = formatJavaDouble(wv.Float())
			}
			return false
		})
		return false
	})
	return val, ok
}

func extractListDefaultReflect(fieldName protoreflect.Name, listMsg protoreflect.Message) (string, bool) {
	valField := listMsg.Descriptor().Fields().ByNumber(1)
	if valField == nil {
		return "", false
	}
	list := listMsg.Get(valField).List()
	var items []string
	for i := 0; i < list.Len(); i++ {
		switch fieldName {
		case "bool_list_value":
			if list.Get(i).Bool() {
				items = append(items, "true")
			} else {
				items = append(items, "false")
			}
		case "string_list_value":
			items = append(items, fmt.Sprintf("%q", list.Get(i).String()))
		case "int64_list_value":
			items = append(items, fmt.Sprintf("%dL", list.Get(i).Int()))
		case "double_list_value":
			items = append(items, formatJavaDouble(list.Get(i).Float()))
		}
	}
	return "java.util.List.of(" + strings.Join(items, ", ") + ")", true
}

