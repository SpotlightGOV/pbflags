// Package javagen generates Java flag client code from feature proto definitions.
package javagen

import (
	"fmt"
	"math"
	"path/filepath"
	"sort"
	"strings"

	"github.com/SpotlightGOV/pbflags/internal/codegen/layerutil"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/encoding/protowire"
	"google.golang.org/protobuf/reflect/protoreflect"
)

type featureEntry struct {
	feat     *featureInfo
	flags    []flagInfo
	msg      *protogen.Message
	protoSrc *protogen.File
}

// Generate produces Java flag client code for all feature messages.
// If dagger is true, generates a Dagger @Module and adds @Inject/@Singleton to impls.
func Generate(plugin *protogen.Plugin, javaPackage string, dagger ...bool) error {
	emitDagger := len(dagger) > 0 && dagger[0]
	_ = emitDagger // used below

	layers, err := layerutil.DiscoverLayers(plugin)
	if err != nil {
		return err
	}

	// Check for case-insensitive outer class name collisions early, before
	// generating any files.
	var genFiles []*protogen.File
	for _, f := range plugin.Files {
		if f.Generate {
			genFiles = append(genFiles, f)
		}
	}
	if err := checkOuterClassCaseCollisions(genFiles); err != nil {
		return err
	}

	var features []featureEntry
	for _, f := range plugin.Files {
		if !f.Generate {
			continue
		}
		for _, msg := range f.Messages {
			feat := extractFeatureOptions(msg)
			if feat == nil {
				continue
			}
			flags, err := extractFlags(msg, layers)
			if err != nil {
				return err
			}
			if len(flags) == 0 {
				continue
			}
			features = append(features, featureEntry{feat: feat, flags: flags, msg: msg, protoSrc: f})
		}
	}
	if len(features) == 0 {
		return nil
	}

	pkgDir := strings.ReplaceAll(javaPackage, ".", "/")

	// Generate layer ID classes for non-global layers.
	if err := generateLayerIDClasses(plugin, layers, javaPackage, pkgDir); err != nil {
		return fmt.Errorf("generating layer ID classes: %w", err)
	}

	for _, entry := range features {
		if err := generateInterface(plugin, entry.feat, entry.flags, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating interface for %s: %w", entry.feat.id, err)
		}
		if err := generateImpl(plugin, entry.feat, entry.flags, javaPackage, pkgDir, emitDagger); err != nil {
			return fmt.Errorf("generating impl for %s: %w", entry.feat.id, err)
		}
	}

	if emitDagger {
		if err := generateDaggerModule(plugin, features, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating Dagger module: %w", err)
		}
	}

	if err := generateDescriptorProvider(plugin, features, javaPackage, pkgDir); err != nil {
		return fmt.Errorf("generating descriptor provider: %w", err)
	}

	return nil
}

// generateLayerIDClasses generates a Java ID wrapper class for each non-global layer.
func generateLayerIDClasses(plugin *protogen.Plugin, layers *layerutil.LayerDef, javaPackage, pkgDir string) error {
	layersPkg := javaPackage + ".layers"
	layersPkgDir := pkgDir + "/layers"

	for _, lv := range layers.Values {
		if lv.IsGlobal {
			continue
		}
		className := layerIDClassName(lv.LayerName)
		outPath := layersPkgDir + "/" + className + ".java"

		g := plugin.NewGeneratedFile(outPath, "")
		p := g.P

		p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
		p("package ", layersPkg, ";")
		p()
		p("/** Typed layer ID for the {@code ", lv.LayerName, "} dimension. */")
		p("public final class ", className, " {")
		p("  private final String value;")
		p("  private ", className, "(String value) { this.value = value; }")
		p("  public static ", className, " of(String id) { return new ", className, "(id); }")
		p("  public static ", className, " global() { return new ", className, "(\"\"); }")
		p("  public String value() { return value; }")
		p("  @Override public String toString() { return value; }")
		p("}")
	}
	return nil
}

// layerIDClassName returns the Java class name for a layer ID (e.g., "user" -> "UserID").
func layerIDClassName(layerName string) string {
	return toPascalCase(layerName) + "ID"
}

func generateInterface(plugin *protogen.Plugin, feat *featureInfo, flags []flagInfo, javaPackage, pkgDir string) error {
	pascalFeat := toPascalCase(feat.id)
	className := pascalFeat + "Flags"
	outPath := pkgDir + "/" + className + ".java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	// Determine what imports we need.
	hasGlobalFlags := false
	hasLayerFlags := false
	hasGlobalListFlags := false
	hasLayerListFlags := false
	layerImports := map[string]bool{}
	for _, fl := range flags {
		if fl.layerName != "" {
			layerImports[fl.layerName] = true
			if fl.isList {
				hasLayerListFlags = true
			} else {
				hasLayerFlags = true
			}
		} else {
			if fl.isList {
				hasGlobalListFlags = true
			} else {
				hasGlobalFlags = true
			}
		}
	}
	hasAnyList := hasGlobalListFlags || hasLayerListFlags

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	if hasAnyList {
		p("import java.util.List;")
	}
	if hasGlobalFlags {
		p("import org.spotlightgov.pbflags.Flag;")
	}
	p("import org.spotlightgov.pbflags.FlagEvaluator;")
	if hasLayerFlags {
		p("import org.spotlightgov.pbflags.LayerFlag;")
	}
	if hasLayerListFlags {
		p("import org.spotlightgov.pbflags.LayerListFlag;")
	}
	if hasGlobalListFlags {
		p("import org.spotlightgov.pbflags.ListFlag;")
	}
	if hasLayerFlags || hasLayerListFlags {
		for _, name := range sortedKeys(layerImports) {
			p("import ", javaPackage, ".layers.", layerIDClassName(name), ";")
		}
	}
	p()
	p("/**")
	p(" * Generated type-safe flag accessors for the {@code ", feat.id, "} feature.")
	if feat.description != "" {
		p(" *")
		p(" * <p>", feat.description)
	}
	p(" */")
	p("public interface ", className, " {")
	p()

	p("  String FEATURE_ID = ", quote(feat.id), ";")
	p()

	for _, fl := range flags {
		p("  String ", fl.constName, "_ID = ", quote(fmt.Sprintf("%s/%d", feat.id, fl.fieldNumber)), ";")
	}
	p()

	for _, fl := range flags {
		if fl.hasDefault {
			p("  ", fl.javaType, " ", fl.constName, "_DEFAULT = ", fl.defaultVal, ";")
		}
	}
	p()

	for _, fl := range flags {
		if fl.description != "" {
			p("  /** ", fl.description, " */")
		}
		if fl.isList {
			if fl.layerName != "" {
				p("  LayerListFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, "();")
			} else {
				p("  ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "();")
			}
		} else {
			if fl.layerName != "" {
				p("  LayerFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, "();")
			} else {
				p("  Flag<", fl.javaBoxedType, "> ", fl.camelName, "();")
			}
		}
		p()
	}

	p("  /**")
	p("   * Creates an instance backed by a {@link FlagEvaluator}.")
	p("   */")
	p("  static ", className, " forEvaluator(FlagEvaluator evaluator) {")
	p("    return new ", className, "() {")
	p()
	for i, fl := range flags {
		p("      @Override")
		if fl.isList {
			if fl.layerName != "" {
				idClass := layerIDClassName(fl.layerName)
				p("      public LayerListFlag<", fl.javaBoxedType, ", ", idClass, "> ", fl.camelName, "() {")
				p("        return evaluator.layerListFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ", ", idClass, "::value);")
			} else {
				p("      public ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
				p("        return evaluator.listFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
			}
		} else {
			if fl.layerName != "" {
				idClass := layerIDClassName(fl.layerName)
				p("      public LayerFlag<", fl.javaBoxedType, ", ", idClass, "> ", fl.camelName, "() {")
				p("        return evaluator.layerFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ", ", idClass, "::value);")
			} else {
				p("      public Flag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
				p("        return evaluator.flag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
			}
		}
		p("      }")
		if i < len(flags)-1 {
			p()
		}
	}
	p("    };")
	p("  }")
	p("}")

	return nil
}

func generateImpl(plugin *protogen.Plugin, feat *featureInfo, flags []flagInfo, javaPackage, pkgDir string, dagger bool) error {
	pascalFeat := toPascalCase(feat.id)
	interfaceName := pascalFeat + "Flags"
	className := pascalFeat + "FlagsImpl"
	outPath := pkgDir + "/" + className + ".java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	// Determine what imports we need.
	hasGlobalFlags := false
	hasLayerFlags := false
	hasGlobalListFlags := false
	hasLayerListFlags := false
	layerImports := map[string]bool{}
	for _, fl := range flags {
		if fl.layerName != "" {
			layerImports[fl.layerName] = true
			if fl.isList {
				hasLayerListFlags = true
			} else {
				hasLayerFlags = true
			}
		} else {
			if fl.isList {
				hasGlobalListFlags = true
			} else {
				hasGlobalFlags = true
			}
		}
	}

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	if hasGlobalFlags {
		p("import org.spotlightgov.pbflags.Flag;")
	}
	p("import org.spotlightgov.pbflags.FlagEvaluator;")
	if hasLayerFlags {
		p("import org.spotlightgov.pbflags.LayerFlag;")
	}
	if hasLayerListFlags {
		p("import org.spotlightgov.pbflags.LayerListFlag;")
	}
	if hasGlobalListFlags {
		p("import org.spotlightgov.pbflags.ListFlag;")
	}
	if hasLayerFlags || hasLayerListFlags {
		for _, name := range sortedKeys(layerImports) {
			p("import ", javaPackage, ".layers.", layerIDClassName(name), ";")
		}
	}
	if dagger {
		p("import javax.inject.Inject;")
		p("import javax.inject.Singleton;")
	}
	p()
	p("/** Generated implementation of {@link ", interfaceName, "}. */")
	if dagger {
		p("@Singleton")
	}
	p("public final class ", className, " implements ", interfaceName, " {")
	p()

	for _, fl := range flags {
		if fl.isList {
			if fl.layerName != "" {
				p("  private final LayerListFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, ";")
			} else {
				p("  private final ListFlag<", fl.javaBoxedType, "> ", fl.camelName, ";")
			}
		} else {
			if fl.layerName != "" {
				p("  private final LayerFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, ";")
			} else {
				p("  private final Flag<", fl.javaBoxedType, "> ", fl.camelName, ";")
			}
		}
	}
	p()

	if dagger {
		p("  @Inject")
	}
	p("  public ", className, "(FlagEvaluator evaluator) {")
	for _, fl := range flags {
		if fl.isList {
			if fl.layerName != "" {
				idClass := layerIDClassName(fl.layerName)
				p("    this.", fl.camelName, " =")
				p("        evaluator.layerListFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ", ", idClass, "::value);")
			} else {
				p("    this.", fl.camelName, " =")
				p("        evaluator.listFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
			}
		} else {
			if fl.layerName != "" {
				idClass := layerIDClassName(fl.layerName)
				p("    this.", fl.camelName, " =")
				p("        evaluator.layerFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ", ", idClass, "::value);")
			} else {
				p("    this.", fl.camelName, " =")
				p("        evaluator.flag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
			}
		}
	}
	p("  }")
	p()

	for _, fl := range flags {
		p("  @Override")
		if fl.isList {
			if fl.layerName != "" {
				p("  public LayerListFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, "() {")
			} else {
				p("  public ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
			}
		} else {
			if fl.layerName != "" {
				p("  public LayerFlag<", fl.javaBoxedType, ", ", layerIDClassName(fl.layerName), "> ", fl.camelName, "() {")
			} else {
				p("  public Flag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
			}
		}
		p("    return ", fl.camelName, ";")
		p("  }")
		p()
	}

	p("}")

	return nil
}

func generateDaggerModule(plugin *protogen.Plugin, features []featureEntry, javaPackage, pkgDir string) error {
	outPath := pkgDir + "/FlagRegistryModule.java"
	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	p("import dagger.Binds;")
	p("import dagger.Module;")
	p()
	p("/** Generated Dagger module binding feature flag interfaces to their implementations. */")
	p("@Module")
	p("public interface FlagRegistryModule {")

	for _, entry := range features {
		pascalFeat := toPascalCase(entry.feat.id)
		interfaceName := pascalFeat + "Flags"
		implName := pascalFeat + "FlagsImpl"
		p()
		p("  @Binds")
		p("  ", interfaceName, " bind", pascalFeat, "Flags(", implName, " impl);")
	}

	p("}")

	return nil
}

func generateDescriptorProvider(plugin *protogen.Plugin, features []featureEntry, javaPackage, pkgDir string) error {
	// Collect unique proto source files.
	seen := map[string]bool{}
	var files []*protogen.File
	for _, entry := range features {
		path := entry.protoSrc.Desc.Path()
		if !seen[path] {
			seen[path] = true
			files = append(files, entry.protoSrc)
		}
	}

	outPath := pkgDir + "/PbflagsFlagDescriptorProvider.java"
	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	p("import com.google.protobuf.Descriptors.FileDescriptor;")
	p("import org.spotlightgov.pbflags.FlagDescriptorProvider;")
	p("import java.util.Arrays;")
	p("import java.util.List;")
	p()
	p("/** Generated {@link FlagDescriptorProvider} for feature flag proto descriptors. */")
	p("public final class PbflagsFlagDescriptorProvider implements FlagDescriptorProvider {")
	p()
	p("  @Override")
	p("  public List<FileDescriptor> fileDescriptors() {")
	p("    return Arrays.asList(")
	for i, f := range files {
		ref := protoOuterClassRef(f)
		comma := ","
		if i == len(files)-1 {
			comma = ""
		}
		p("        ", ref, ".getDescriptor()", comma)
	}
	p("    );")
	p("  }")
	p("}")

	return nil
}

// checkOuterClassCaseCollisions detects case-insensitive collisions between
// derived Java outer class names and top-level declarations. On case-insensitive
// filesystems (macOS APFS default), these produce silent file overwrites that
// result in uncompilable Java code. protoc only checks exact (case-sensitive)
// matches, so this fills the gap.
func checkOuterClassCaseCollisions(files []*protogen.File) error {
	for _, f := range files {
		// Explicit java_outer_classname means the user has already handled it.
		if f.Proto.GetOptions().GetJavaOuterClassname() != "" {
			continue
		}

		base := filepath.Base(f.Proto.GetName())
		base = strings.TrimSuffix(base, filepath.Ext(base))
		outerClass := toUnderscoreCamelCase(base)

		var declNames []string
		for _, msg := range f.Messages {
			declNames = append(declNames, string(msg.Desc.Name()))
		}
		for _, e := range f.Enums {
			declNames = append(declNames, string(e.Desc.Name()))
		}
		for _, s := range f.Services {
			declNames = append(declNames, string(s.Desc.Name()))
		}

		if collider := outerClassCaseCollision(outerClass, declNames); collider != "" {
			return fmt.Errorf(
				"protoc-gen-pbflags: %s: derived Java outer class %q case-insensitively collides "+
					"with %q — on case-insensitive filesystems (macOS APFS) this causes file overwrites "+
					"and uncompilable Java code. Fix: add 'option java_outer_classname = \"...\";' to %s",
				f.Proto.GetName(), outerClass, collider, f.Proto.GetName())
		}
	}
	return nil
}

// outerClassCaseCollision checks whether outerClass case-insensitively matches
// any name in declNames that is NOT an exact match (exact matches are already
// handled by protoFileToOuterClass appending "OuterClass"). Returns the
// colliding name or "".
func outerClassCaseCollision(outerClass string, declNames []string) string {
	for _, name := range declNames {
		if name != outerClass && strings.EqualFold(name, outerClass) {
			return name
		}
	}
	return ""
}

// protoOuterClassRef returns the fully qualified Java outer class reference for a proto file.
// This is the class that holds the static getDescriptor() method for the file.
func protoOuterClassRef(f *protogen.File) string {
	javaPkg := f.Proto.GetOptions().GetJavaPackage()
	if javaPkg == "" {
		javaPkg = string(f.Proto.GetPackage())
	}

	outerClass := f.Proto.GetOptions().GetJavaOuterClassname()
	if outerClass == "" {
		outerClass = protoFileToOuterClass(f)
	}

	return javaPkg + "." + outerClass
}

// protoFileToOuterClass derives the Java outer class name from a proto file,
// following protoc's algorithm: CamelCase the filename, append "OuterClass" if
// it conflicts with a top-level message, enum, or service name.
func protoFileToOuterClass(f *protogen.File) string {
	base := filepath.Base(f.Proto.GetName())
	base = strings.TrimSuffix(base, filepath.Ext(base))
	name := toUnderscoreCamelCase(base)

	// Check for conflicts with top-level declarations.
	for _, msg := range f.Messages {
		if string(msg.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}
	for _, e := range f.Enums {
		if string(e.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}
	for _, s := range f.Services {
		if string(s.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}

	return name
}

// toUnderscoreCamelCase converts a proto filename base (e.g. "my_file") to CamelCase ("MyFile"),
// matching protoc's java_outer_classname derivation.
func toUnderscoreCamelCase(s string) string {
	var result strings.Builder
	upper := true
	for _, c := range s {
		if c == '_' || c == '-' {
			upper = true
			continue
		}
		if upper {
			result.WriteRune(rune(strings.ToUpper(string(c))[0]))
			upper = false
		} else {
			result.WriteRune(c)
		}
	}
	return result.String()
}

// --- Types ---

type featureInfo struct {
	id          string
	description string
	owner       string
}

type flagInfo struct {
	camelName        string
	constName        string
	fieldNumber      int32
	javaType         string
	javaBoxedType    string
	javaClassLiteral string
	defaultVal       string
	hasDefault       bool
	isList           bool // true for repeated (list-valued) flags
	description      string
	layerName        string // empty = global; otherwise the lowercase layer name (e.g., "user", "entity")
}

func javaDefaultExpr(fl flagInfo) string {
	if fl.hasDefault {
		return fl.constName + "_DEFAULT"
	}
	if fl.isList {
		return "java.util.List.of()"
	}
	switch fl.javaType {
	case "boolean":
		return "false"
	case "String":
		return `""`
	case "long":
		return "0L"
	case "double":
		return "0.0"
	default:
		return "null"
	}
}

// --- Extraction (mirrors gogen) ---

func extractFeatureOptions(msg *protogen.Message) *featureInfo {
	opts := msg.Desc.Options()
	if opts == nil {
		return nil
	}
	protoMsg := opts.(interface{ ProtoReflect() protoreflect.Message })
	rm := protoMsg.ProtoReflect()

	var feat *featureInfo
	rm.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		if fd.Number() == 51000 && fd.IsExtension() {
			m := v.Message()
			info := &featureInfo{}
			m.Range(func(innerFd protoreflect.FieldDescriptor, innerV protoreflect.Value) bool {
				switch innerFd.Name() {
				case "id":
					info.id = innerV.String()
				case "description":
					info.description = innerV.String()
				case "owner":
					info.owner = innerV.String()
				}
				return true
			})
			if info.id != "" {
				feat = info
			}
			return false
		}
		return true
	})
	if feat != nil {
		return feat
	}

	unk := rm.GetUnknown()
	if len(unk) == 0 {
		return nil
	}
	return parseFeatureFromUnknown(unk)
}

func parseFeatureFromUnknown(b []byte) *featureInfo {
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		if num == 51000 && typ == protowire.BytesType {
			data, n := protowire.ConsumeBytes(b)
			if n < 0 {
				return nil
			}
			return parseFeatureMessage(data)
		}
		n = skipField(b, typ)
		if n < 0 {
			return nil
		}
		b = b[n:]
	}
	return nil
}

func parseFeatureMessage(b []byte) *featureInfo {
	info := &featureInfo{}
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		if typ != protowire.BytesType {
			n = skipField(b, typ)
			if n < 0 {
				return nil
			}
			b = b[n:]
			continue
		}
		data, n := protowire.ConsumeBytes(b)
		if n < 0 {
			return nil
		}
		b = b[n:]
		switch num {
		case 1:
			info.id = string(data)
		case 2:
			info.description = string(data)
		case 3:
			info.owner = string(data)
		}
	}
	if info.id == "" {
		return nil
	}
	return info
}

func extractFlags(msg *protogen.Message, layers *layerutil.LayerDef) ([]flagInfo, error) {
	var flags []flagInfo
	for _, field := range msg.Fields {
		fl, err := extractFlagFromField(field, layers)
		if err != nil {
			return nil, err
		}
		if fl != nil {
			flags = append(flags, *fl)
		}
	}
	return flags, nil
}

func extractFlagFromField(field *protogen.Field, layers *layerutil.LayerDef) (*flagInfo, error) {
	opts := field.Desc.Options()
	if opts == nil {
		return nil, nil
	}
	protoMsg := opts.(interface{ ProtoReflect() protoreflect.Message })
	rm := protoMsg.ProtoReflect()

	var found bool
	var layerStr string
	var defaultVal string
	var hasDefault bool
	var description string

	rm.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		if fd.Number() == 51001 && fd.IsExtension() {
			found = true
			m := v.Message()
			m.Range(func(innerFd protoreflect.FieldDescriptor, innerV protoreflect.Value) bool {
				switch innerFd.Name() {
				case "layer":
					layerStr = innerV.String()
				case "default":
					defaultVal, hasDefault = extractDefaultReflect(innerV.Message(), field.Desc.Kind())
				case "description":
					description = innerV.String()
				}
				return true
			})
			return false
		}
		return true
	})

	if !found {
		unk := rm.GetUnknown()
		if len(unk) == 0 {
			return nil, nil
		}
		found, layerStr, defaultVal, hasDefault, description = parseFlagFromUnknown(unk, field.Desc.Kind())
		if !found {
			return nil, nil
		}
	}

	// Validate the layer against the discovered enum.
	var resolvedLayerName string
	if layerStr != "" {
		lv, ok := layers.ResolveLayer(layerStr)
		if !ok {
			return nil, fmt.Errorf("flag %s.%s: layer %q does not match any value in the %s enum",
				field.Parent.Desc.Name(), field.Desc.Name(), layerStr, layers.EnumName)
		}
		if !lv.IsGlobal {
			resolvedLayerName = lv.LayerName
		}
	}

	fieldName := string(field.Desc.Name())
	isList := field.Desc.IsList()
	javaType, javaBoxedType, javaClassLiteral := javaTypeInfo(field.Desc.Kind(), isList)

	return &flagInfo{
		camelName:        toCamelCase(fieldName),
		constName:        toScreamingSnake(fieldName),
		fieldNumber:      int32(field.Desc.Number()),
		javaType:         javaType,
		javaBoxedType:    javaBoxedType,
		javaClassLiteral: javaClassLiteral,
		defaultVal:       defaultVal,
		hasDefault:       hasDefault,
		isList:           isList,
		description:      description,
		layerName:        resolvedLayerName,
	}, nil
}

func extractDefaultReflect(defMsg protoreflect.Message, fieldKind protoreflect.Kind) (string, bool) {
	var val string
	var ok bool
	defMsg.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		switch fd.Name() {
		case "bool_list_value", "string_list_value", "int64_list_value", "double_list_value":
			val, ok = extractListDefaultReflect(fd.Name(), v.Message())
			return false
		}
		wrapperMsg := v.Message()
		wrapperMsg.Range(func(wfd protoreflect.FieldDescriptor, wv protoreflect.Value) bool {
			if wfd.Name() != "value" {
				return true
			}
			switch fd.Name() {
			case "bool_value":
				ok = true
				if wv.Bool() {
					val = "true"
				} else {
					val = "false"
				}
			case "string_value":
				ok = true
				val = fmt.Sprintf("%q", wv.String())
			case "int64_value":
				ok = true
				val = fmt.Sprintf("%dL", wv.Int())
			case "double_value":
				ok = true
				val = formatJavaDouble(wv.Float())
			}
			return false
		})
		return false
	})
	return val, ok
}

func extractListDefaultReflect(fieldName protoreflect.Name, listMsg protoreflect.Message) (string, bool) {
	valField := listMsg.Descriptor().Fields().ByNumber(1)
	if valField == nil {
		return "", false
	}
	list := listMsg.Get(valField).List()
	var items []string
	for i := 0; i < list.Len(); i++ {
		switch fieldName {
		case "bool_list_value":
			if list.Get(i).Bool() {
				items = append(items, "true")
			} else {
				items = append(items, "false")
			}
		case "string_list_value":
			items = append(items, fmt.Sprintf("%q", list.Get(i).String()))
		case "int64_list_value":
			items = append(items, fmt.Sprintf("%dL", list.Get(i).Int()))
		case "double_list_value":
			items = append(items, formatJavaDouble(list.Get(i).Float()))
		}
	}
	return "java.util.List.of(" + strings.Join(items, ", ") + ")", true
}

func parseFlagFromUnknown(b []byte, fieldKind protoreflect.Kind) (found bool, layerStr string, defaultVal string, hasDefault bool, description string) {
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return
		}
		b = b[n:]
		if num == 51001 && typ == protowire.BytesType {
			data, n := protowire.ConsumeBytes(b)
			if n < 0 {
				return
			}
			found = true
			layerStr, defaultVal, hasDefault, description = parseFlagOptionsMessage(data, fieldKind)
			return
		}
		n = skipField(b, typ)
		if n < 0 {
			return
		}
		b = b[n:]
	}
	return
}

func parseFlagOptionsMessage(b []byte, fieldKind protoreflect.Kind) (layerStr string, defaultVal string, hasDefault bool, description string) {
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return
		}
		b = b[n:]
		switch num {
		case 1:
			if typ == protowire.BytesType {
				data, n := protowire.ConsumeBytes(b)
				if n < 0 {
					return
				}
				b = b[n:]
				description = string(data)
				continue
			}
		case 2:
			if typ == protowire.BytesType {
				data, n := protowire.ConsumeBytes(b)
				if n < 0 {
					return
				}
				b = b[n:]
				defaultVal, hasDefault = parseFlagDefault(data, fieldKind)
				continue
			}
		case 5: // string layer field
			if typ == protowire.BytesType {
				data, n := protowire.ConsumeBytes(b)
				if n < 0 {
					return
				}
				b = b[n:]
				layerStr = string(data)
				continue
			}
		}
		n = skipField(b, typ)
		if n < 0 {
			return
		}
		b = b[n:]
	}
	return
}

func parseFlagDefault(b []byte, fieldKind protoreflect.Kind) (string, bool) {
	for len(b) > 0 {
		num, typ, n := protowire.ConsumeTag(b)
		if n < 0 {
			return "", false
		}
		b = b[n:]
		if typ == protowire.BytesType {
			data, n := protowire.ConsumeBytes(b)
			if n < 0 {
				return "", false
			}
			b = b[n:]
			switch num {
			case 1:
				return parseBoolWrapper(data)
			case 2:
				return parseStringWrapper(data)
			case 3:
				return parseInt64Wrapper(data)
			case 4:
				return parseDoubleWrapper(data)
			case 5:
				return parseBoolListMessage(data)
			case 6:
				return parseStringListMessage(data)
			case 7:
				return parseInt64ListMessage(data)
			case 8:
				return parseDoubleListMessage(data)
			}
			continue
		}
		n = skipField(b, typ)
		if n < 0 {
			return "", false
		}
		b = b[n:]
	}
	return "", false
}

