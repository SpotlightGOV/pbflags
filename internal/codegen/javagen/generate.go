// Package javagen generates Java flag client code from feature proto definitions.
package javagen

import (
	"fmt"
	"math"
	"path/filepath"
	"strings"

	"github.com/SpotlightGOV/pbflags/internal/codegen/contextutil"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/encoding/protowire"
	"google.golang.org/protobuf/reflect/protoreflect"
)

type featureEntry struct {
	feat     *featureInfo
	flags    []flagInfo
	msg      *protogen.Message
	protoSrc *protogen.File
}

// Generate produces Java flag client code for all feature messages.
// If dagger is true, generates a Dagger @Module and adds @Inject/@Singleton to impls.
func Generate(plugin *protogen.Plugin, javaPackage string, dagger ...bool) error {
	emitDagger := len(dagger) > 0 && dagger[0]
	_ = emitDagger // used below

	ctxDef, err := contextutil.DiscoverContext(plugin)
	if err != nil {
		return fmt.Errorf("discovering evaluation context: %w", err)
	}
	if ctxDef != nil {
		if err := generateDimensionsClass(plugin, ctxDef, javaPackage); err != nil {
			return fmt.Errorf("generating Dimensions class: %w", err)
		}
	}

	// Check for case-insensitive outer class name collisions early, before
	// generating any files.
	var genFiles []*protogen.File
	for _, f := range plugin.Files {
		if f.Generate {
			genFiles = append(genFiles, f)
		}
	}
	if err := checkOuterClassCaseCollisions(genFiles); err != nil {
		return err
	}

	var features []featureEntry
	for _, f := range plugin.Files {
		if !f.Generate {
			continue
		}
		for _, msg := range f.Messages {
			feat := extractFeatureOptions(msg)
			if feat == nil {
				continue
			}
			flags, err := extractFlags(msg)
			if err != nil {
				return err
			}
			if len(flags) == 0 {
				continue
			}
			features = append(features, featureEntry{feat: feat, flags: flags, msg: msg, protoSrc: f})
		}
	}
	if len(features) == 0 {
		return nil
	}

	pkgDir := strings.ReplaceAll(javaPackage, ".", "/")

	for _, entry := range features {
		if err := generateInterface(plugin, entry.feat, entry.flags, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating interface for %s: %w", entry.feat.id, err)
		}
		if err := generateImpl(plugin, entry.feat, entry.flags, javaPackage, pkgDir, emitDagger); err != nil {
			return fmt.Errorf("generating impl for %s: %w", entry.feat.id, err)
		}
		if err := generateTestImpl(plugin, entry.feat, entry.flags, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating test impl for %s: %w", entry.feat.id, err)
		}
	}

	if emitDagger {
		if err := generateDaggerModule(plugin, features, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating Dagger module: %w", err)
		}
		if err := generateScopedEvaluatorModule(plugin, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating scoped evaluator module: %w", err)
		}
	}

	if err := generateDescriptorProvider(plugin, features, javaPackage, pkgDir); err != nil {
		return fmt.Errorf("generating descriptor provider: %w", err)
	}

	// Generate per-scope Features classes if scopes are defined.
	scopes := contextutil.DiscoverScopes(plugin)
	if ctxDef != nil && len(scopes) > 0 {
		if err := generateScopeClasses(plugin, ctxDef, scopes, features, javaPackage, pkgDir); err != nil {
			return fmt.Errorf("generating scope classes: %w", err)
		}
	}

	return nil
}

func generateInterface(plugin *protogen.Plugin, feat *featureInfo, flags []flagInfo, javaPackage, pkgDir string) error {
	pascalFeat := toPascalCase(feat.id)
	className := pascalFeat + "Flags"
	outPath := pkgDir + "/" + className + ".java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	// Determine what imports we need.
	hasFlags := false
	hasListFlags := false
	for _, fl := range flags {
		if fl.isList {
			hasListFlags = true
		} else {
			hasFlags = true
		}
	}

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	if hasListFlags {
		p("import java.util.List;")
	}
	if hasFlags {
		p("import org.spotlightgov.pbflags.Flag;")
	}
	p("import org.spotlightgov.pbflags.FlagEvaluator;")
	if hasListFlags {
		p("import org.spotlightgov.pbflags.ListFlag;")
	}
	p()
	p("/**")
	p(" * Generated type-safe flag accessors for the {@code ", feat.id, "} feature.")
	if feat.description != "" {
		p(" *")
		p(" * <p>", feat.description)
	}
	p(" */")
	p("public interface ", className, " {")
	p()

	p("  String FEATURE_ID = ", quote(feat.id), ";")
	p()

	for _, fl := range flags {
		p("  String ", fl.constName, "_ID = ", quote(fmt.Sprintf("%s/%d", feat.id, fl.fieldNumber)), ";")
	}
	p()

	for _, fl := range flags {
		if fl.hasDefault {
			p("  ", fl.javaType, " ", fl.constName, "_DEFAULT = ", fl.defaultVal, ";")
		}
	}
	p()

	for _, fl := range flags {
		if fl.description != "" {
			p("  /** ", fl.description, " */")
		}
		if fl.isList {
			p("  ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "();")
		} else {
			p("  Flag<", fl.javaBoxedType, "> ", fl.camelName, "();")
		}
		p()
	}

	p("  /**")
	p("   * Creates an instance backed by a {@link FlagEvaluator}.")
	p("   */")
	p("  static ", className, " forEvaluator(FlagEvaluator evaluator) {")
	p("    return new ", className, "() {")
	p()
	for i, fl := range flags {
		p("      @Override")
		if fl.isList {
			p("      public ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
			p("        return evaluator.listFlag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
		} else {
			p("      public Flag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
			p("        return evaluator.flag(", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", javaDefaultExpr(fl), ");")
		}
		p("      }")
		if i < len(flags)-1 {
			p()
		}
	}
	p("    };")
	p("  }")
	p("}")

	return nil
}

func generateImpl(plugin *protogen.Plugin, feat *featureInfo, flags []flagInfo, javaPackage, pkgDir string, dagger bool) error {
	pascalFeat := toPascalCase(feat.id)
	interfaceName := pascalFeat + "Flags"
	className := pascalFeat + "FlagsImpl"
	outPath := pkgDir + "/" + className + ".java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	// Determine what imports we need.
	hasFlags := false
	hasListFlags := false
	for _, fl := range flags {
		if fl.isList {
			hasListFlags = true
		} else {
			hasFlags = true
		}
	}

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	if hasFlags {
		p("import org.spotlightgov.pbflags.Flag;")
	}
	p("import org.spotlightgov.pbflags.FlagEvaluator;")
	if hasListFlags {
		p("import org.spotlightgov.pbflags.ListFlag;")
	}
	p("import org.slf4j.Logger;")
	p("import org.slf4j.LoggerFactory;")
	if dagger {
		p("import javax.inject.Inject;")
		p("import javax.inject.Singleton;")
	}
	p()
	p("/** Generated implementation of {@link ", interfaceName, "}. */")
	if dagger {
		p("@Singleton")
	}
	p("public final class ", className, " implements ", interfaceName, " {")
	p()
	p("  private static final Logger logger = LoggerFactory.getLogger(", className, ".class);")
	p()

	for _, fl := range flags {
		if fl.isList {
			p("  private final ListFlag<", fl.javaBoxedType, "> ", fl.camelName, ";")
		} else {
			p("  private final Flag<", fl.javaBoxedType, "> ", fl.camelName, ";")
		}
	}
	p()

	if dagger {
		p("  @Inject")
	}
	p("  public ", className, "(FlagEvaluator evaluator) {")
	for _, fl := range flags {
		defExpr := javaDefaultExpr(fl)
		if fl.isList {
			p("    this.", fl.camelName, " = safeListFlag(evaluator, ", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", defExpr, ");")
		} else {
			p("    this.", fl.camelName, " = safeFlag(evaluator, ", fl.constName, "_ID, ", fl.javaClassLiteral, ", ", defExpr, ");")
		}
	}
	p("  }")
	p()

	for _, fl := range flags {
		p("  @Override")
		if fl.isList {
			p("  public ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
		} else {
			p("  public Flag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
		}
		p("    return ", fl.camelName, ";")
		p("  }")
		p()
	}

	// Helper methods that wrap evaluator calls with logging.
	if hasFlags {
		p("  private static <T> Flag<T> safeFlag(FlagEvaluator evaluator, String flagId,")
		p("      Class<T> type, T compiledDefault) {")
		p("    return () -> {")
		p("      try {")
		p("        T value = evaluator.evaluate(flagId, type, compiledDefault);")
		p("        if (logger.isDebugEnabled()) {")
		p("          logger.debug(\"flag evaluated: {} = {} ({})\", flagId, value, type.getSimpleName());")
		p("        }")
		p("        return value;")
		p("      } catch (Exception e) {")
		p("        logger.error(\"Flag evaluation failed for {}, returning default\", flagId, e);")
		p("        return compiledDefault;")
		p("      }")
		p("    };")
		p("  }")
		p()
	}
	if hasListFlags {
		p("  private static <E> ListFlag<E> safeListFlag(FlagEvaluator evaluator, String flagId,")
		p("      Class<E> elementType, java.util.List<E> compiledDefault) {")
		p("    return () -> {")
		p("      try {")
		p("        java.util.List<E> value = evaluator.evaluateList(flagId, elementType, compiledDefault);")
		p("        if (logger.isDebugEnabled()) {")
		p("          logger.debug(\"list flag evaluated: {} = {} (List<{}>)\", flagId, value, elementType.getSimpleName());")
		p("        }")
		p("        return value;")
		p("      } catch (Exception e) {")
		p("        logger.error(\"List flag evaluation failed for {}, returning default\", flagId, e);")
		p("        return compiledDefault;")
		p("      }")
		p("    };")
		p("  }")
		p()
	}

	p("}")

	return nil
}

func generateTestImpl(plugin *protogen.Plugin, feat *featureInfo, flags []flagInfo, javaPackage, pkgDir string) error {
	pascalFeat := toPascalCase(feat.id)
	interfaceName := pascalFeat + "Flags"
	className := "Test" + pascalFeat + "Flags"
	outPath := pkgDir + "/" + className + ".java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	hasFlags := false
	hasListFlags := false
	for _, fl := range flags {
		if fl.isList {
			hasListFlags = true
		} else {
			hasFlags = true
		}
	}

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	if hasListFlags {
		p("import java.util.List;")
	}
	if hasFlags {
		p("import org.spotlightgov.pbflags.Flag;")
	}
	if hasListFlags {
		p("import org.spotlightgov.pbflags.ListFlag;")
	}
	p()
	p("/**")
	p(" * Mutable test stub for {@link ", interfaceName, "}.")
	p(" *")
	p(" * <p>Each flag accessor delegates to its corresponding field, which is pre-populated with the")
	p(" * compiled default. Override individual flags via the fluent setters.")
	p(" */")
	p("public final class ", className, " implements ", interfaceName, " {")
	p()

	// Fields: one per flag, pre-populated with compiled defaults.
	for _, fl := range flags {
		if fl.isList {
			p("  private ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "Fn = () -> ", javaDefaultExpr(fl), ";")
		} else {
			p("  private Flag<", fl.javaBoxedType, "> ", fl.camelName, "Fn = () -> ", javaDefaultExpr(fl), ";")
		}
	}
	p()

	// Fluent value setters.
	for _, fl := range flags {
		if fl.isList {
			p("  /** Override {@code ", fl.camelName, "()} to return the given value. */")
			p("  public ", className, " ", fl.camelName, "(List<", fl.javaBoxedType, "> value) {")
			p("    this.", fl.camelName, "Fn = () -> value;")
			p("    return this;")
			p("  }")
			p()
		} else {
			p("  /** Override {@code ", fl.camelName, "()} to return the given value. */")
			p("  public ", className, " ", fl.camelName, "(", fl.javaType, " value) {")
			p("    this.", fl.camelName, "Fn = () -> value;")
			p("    return this;")
			p("  }")
			p()
		}
	}

	// Accessor methods delegating to the Fn fields.
	for _, fl := range flags {
		p("  @Override")
		if fl.isList {
			p("  public ListFlag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
		} else {
			p("  public Flag<", fl.javaBoxedType, "> ", fl.camelName, "() {")
		}
		p("    return ", fl.camelName, "Fn;")
		p("  }")
		p()
	}

	p("}")

	return nil
}

func generateDaggerModule(plugin *protogen.Plugin, features []featureEntry, javaPackage, pkgDir string) error {
	outPath := pkgDir + "/FlagRegistryModule.java"
	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	p("import dagger.Binds;")
	p("import dagger.Module;")
	p()
	p("/** Generated Dagger module binding feature flag interfaces to their implementations. */")
	p("@Module")
	p("public interface FlagRegistryModule {")

	for _, entry := range features {
		pascalFeat := toPascalCase(entry.feat.id)
		interfaceName := pascalFeat + "Flags"
		implName := pascalFeat + "FlagsImpl"
		p()
		p("  @Binds")
		p("  ", interfaceName, " bind", pascalFeat, "Flags(", implName, " impl);")
	}

	p("}")

	return nil
}

