func generateDescriptorProvider(plugin *protogen.Plugin, features []featureEntry, javaPackage, pkgDir string) error {
	// Collect unique proto source files.
	seen := map[string]bool{}
	var files []*protogen.File
	for _, entry := range features {
		path := entry.protoSrc.Desc.Path()
		if !seen[path] {
			seen[path] = true
			files = append(files, entry.protoSrc)
		}
	}

	outPath := pkgDir + "/PbflagsFlagDescriptorProvider.java"
	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	p("import com.google.protobuf.Descriptors.FileDescriptor;")
	p("import org.spotlightgov.pbflags.FlagDescriptorProvider;")
	p("import java.util.Arrays;")
	p("import java.util.List;")
	p()
	p("/** Generated {@link FlagDescriptorProvider} for feature flag proto descriptors. */")
	p("public final class PbflagsFlagDescriptorProvider implements FlagDescriptorProvider {")
	p()
	p("  @Override")
	p("  public List<FileDescriptor> fileDescriptors() {")
	p("    return Arrays.asList(")
	for i, f := range files {
		ref := protoOuterClassRef(f)
		comma := ","
		if i == len(files)-1 {
			comma = ""
		}
		p("        ", ref, ".getDescriptor()", comma)
	}
	p("    );")
	p("  }")
	p("}")

	return nil
}

// generateDimensionsClass generates a Dims.java class with typed dimension
// constructors from the EvaluationContext proto message.
func generateDimensionsClass(plugin *protogen.Plugin, ctx *contextutil.ContextDef, javaPackage string) error {
	pkgDir := strings.ReplaceAll(javaPackage, ".", "/")
	outPath := pkgDir + "/Dimensions.java"

	g := plugin.NewGeneratedFile(outPath, "")
	p := g.P

	p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
	p("package ", javaPackage, ";")
	p()
	p("import org.spotlightgov.pbflags.Dimension;")
	p()
	p("/** Generated typed dimension constructors for the EvaluationContext. */")
	p("public final class Dimensions {")
	p()
	p("  private Dimensions() {}")

	for _, dim := range ctx.Dimensions {
		methodName := toCamelCase(dim.Name)
		desc := dim.Description
		if desc == "" {
			desc = dim.Name + " dimension"
		}

		p()
		switch dim.Kind {
		case contextutil.DimensionString:
			p("  /** Sets the ", dim.Name, " dimension (", desc, "). */")
			p("  public static Dimension ", methodName, "(String v) {")
			p("    return Dimension.ofString(", quote(dim.Name), ", v);")
			p("  }")

		case contextutil.DimensionBool:
			p("  /** Sets the ", dim.Name, " dimension (", desc, "). */")
			p("  public static Dimension ", methodName, "(boolean v) {")
			p("    return Dimension.ofBool(", quote(dim.Name), ", v);")
			p("  }")

		case contextutil.DimensionInt64:
			p("  /** Sets the ", dim.Name, " dimension (", desc, "). */")
			p("  public static Dimension ", methodName, "(long v) {")
			p("    return Dimension.ofInt64(", quote(dim.Name), ", v);")
			p("  }")

		case contextutil.DimensionEnum:
			// Derive the Java enum class reference from the proto enum.
			javaEnumRef := javaEnumReference(dim.ProtoField)
			p("  /** Sets the ", dim.Name, " dimension (", desc, "). */")
			p("  public static Dimension ", methodName, "(", javaEnumRef, " v) {")
			p("    return Dimension.ofEnum(", quote(dim.Name), ", v.getValueDescriptor());")
			p("  }")
		}
	}

	p("}")

	return nil
}

// generateScopeClasses generates per-scope Java classes (e.g., AnonFeatures, UserFeatures).
func generateScopeClasses(
	plugin *protogen.Plugin,
	ctxDef *contextutil.ContextDef,
	scopes []contextutil.ScopeDef,
	features []featureEntry,
	javaPackage, pkgDir string,
) error {
	dimByName := map[string]contextutil.DimensionDef{}
	for _, d := range ctxDef.Dimensions {
		dimByName[d.Name] = d
	}

	var globalDims []contextutil.DimensionDef
	for _, d := range ctxDef.Dimensions {
		if d.IsRequired() {
			globalDims = append(globalDims, d)
		}
	}

	featureScopeSet := map[string]map[string]bool{}
	for _, e := range features {
		featureScopeSet[e.feat.id] = map[string]bool{}
		for _, s := range e.feat.scopes {
			featureScopeSet[e.feat.id][s] = true
		}
	}

	for _, scope := range scopes {
		scopePascal := toPascalCase(scope.Name)
		className := scopePascal + "Features"

		var scopeDims []contextutil.DimensionDef
		scopeDims = append(scopeDims, globalDims...)
		for _, dimName := range scope.Dimensions {
			if d, ok := dimByName[dimName]; ok {
				scopeDims = append(scopeDims, d)
			}
		}

		var scopeFeatures []featureEntry
		for _, e := range features {
			if featureScopeSet[e.feat.id][scope.Name] {
				scopeFeatures = append(scopeFeatures, e)
			}
		}
		if len(scopeFeatures) == 0 {
			continue
		}

		outPath := pkgDir + "/" + className + ".java"
		g := plugin.NewGeneratedFile(outPath, "")
		p := g.P

		p("// Code generated by protoc-gen-pbflags. DO NOT EDIT.")
		p("package ", javaPackage, ";")
		p()
		p("import org.spotlightgov.pbflags.Dimension;")
		p("import org.spotlightgov.pbflags.FlagEvaluator;")
		p()
		p("/**")
		p(" * Feature flag accessor for the {@code ", scope.Name, "} evaluation scope.")
		p(" *")
		p(" * <p>Constructor requires all dimensions available in this scope.")
		p(" * Feature accessors are cached on first access.")
		p(" */")
		p("public final class ", className, " {")
		p()
		p("  private final FlagEvaluator eval;")
		for _, d := range scopeDims {
			jType := javaDimType(d)
			p("  private final ", jType, " ", toCamelCase(d.Name), ";")
		}
		for _, e := range scopeFeatures {
			ifaceName := toPascalCase(e.feat.id) + "Flags"
			p("  private ", ifaceName, " cached", toPascalCase(e.feat.id), ";")
		}
		p()

		// Constructor.
		var ctorParams []string
		ctorParams = append(ctorParams, "FlagEvaluator eval")
		for _, d := range scopeDims {
			ctorParams = append(ctorParams, javaDimType(d)+" "+toCamelCase(d.Name))
		}
		p("  public ", className, "(", strings.Join(ctorParams, ", "), ") {")
		p("    this.eval = eval;")
		for _, d := range scopeDims {
			camel := toCamelCase(d.Name)
			p("    this.", camel, " = ", camel, ";")
		}
		p("  }")
		p()

		// Feature accessors.
		for _, e := range scopeFeatures {
			pascal := toPascalCase(e.feat.id)
			ifaceName := pascal + "Flags"
			cachedField := "cached" + pascal

			p("  /** Returns a {@link ", ifaceName, "} client scoped to this evaluation context. */")
			p("  public ", ifaceName, " ", toCamelCase(e.feat.id), "() {")
			p("    if (this.", cachedField, " == null) {")
			p("      FlagEvaluator scoped = this.eval;")
			for _, d := range scopeDims {
				dimMethod := toCamelCase(d.Name)
				p("      scoped = scoped.with(Dimensions.", dimMethod, "(this.", dimMethod, "));")
			}
			p("      this.", cachedField, " = new ", pascal, "FlagsImpl(scoped);")
			p("    }")
			p("    return this.", cachedField, ";")
			p("  }")
			p()
		}

		p("}")
	}

	return nil
}

// javaDimType returns the Java type for a dimension kind.
func javaDimType(d contextutil.DimensionDef) string {
	switch d.Kind {
	case contextutil.DimensionBool:
		return "boolean"
	case contextutil.DimensionInt64:
		return "long"
	case contextutil.DimensionEnum:
		if d.ProtoField != nil {
			return javaEnumReference(d.ProtoField)
		}
		return "int"
	default:
		return "String"
	}
}

// javaEnumReference returns the fully qualified Java class name for a proto enum field.
// Reads java_package, java_multiple_files, and java_outer_classname from file options
// via protoreflect to derive the correct Java reference.
func javaEnumReference(field *protogen.Field) string {
	enumDesc := field.Desc.Enum()
	fileDesc := enumDesc.ParentFile()
	enumName := string(enumDesc.Name())

	// Read Java file options via protoreflect.
	var javaPkg string
	var javaMultipleFiles bool
	var javaOuterClassname string

	rm := fileDesc.Options().ProtoReflect()
	rm.Range(func(fd protoreflect.FieldDescriptor, v protoreflect.Value) bool {
		switch fd.Name() {
		case "java_package":
			javaPkg = v.String()
		case "java_multiple_files":
			javaMultipleFiles = v.Bool()
		case "java_outer_classname":
			javaOuterClassname = v.String()
		}
		return true
	})

	if javaPkg == "" {
		javaPkg = string(fileDesc.Package())
	}

	if javaMultipleFiles {
		return javaPkg + "." + enumName
	}

	if javaOuterClassname == "" {
		base := filepath.Base(string(fileDesc.Path()))
		base = strings.TrimSuffix(base, filepath.Ext(base))
		javaOuterClassname = toUnderscoreCamelCase(base)
	}

	return javaPkg + "." + javaOuterClassname + "." + enumName
}

// checkOuterClassCaseCollisions detects case-insensitive collisions between
// derived Java outer class names and top-level declarations. On case-insensitive
// filesystems (macOS APFS default), these produce silent file overwrites that
// result in uncompilable Java code. protoc only checks exact (case-sensitive)
// matches, so this fills the gap.
func checkOuterClassCaseCollisions(files []*protogen.File) error {
	for _, f := range files {
		// Explicit java_outer_classname means the user has already handled it.
		if f.Proto.GetOptions().GetJavaOuterClassname() != "" {
			continue
		}

		base := filepath.Base(f.Proto.GetName())
		base = strings.TrimSuffix(base, filepath.Ext(base))
		outerClass := toUnderscoreCamelCase(base)

		var declNames []string
		for _, msg := range f.Messages {
			declNames = append(declNames, string(msg.Desc.Name()))
		}
		for _, e := range f.Enums {
			declNames = append(declNames, string(e.Desc.Name()))
		}
		for _, s := range f.Services {
			declNames = append(declNames, string(s.Desc.Name()))
		}

		if collider := outerClassCaseCollision(outerClass, declNames); collider != "" {
			return fmt.Errorf(
				"protoc-gen-pbflags: %s: derived Java outer class %q case-insensitively collides "+
					"with %q — on case-insensitive filesystems (macOS APFS) this causes file overwrites "+
					"and uncompilable Java code. Fix: add 'option java_outer_classname = \"...\";' to %s",
				f.Proto.GetName(), outerClass, collider, f.Proto.GetName())
		}
	}
	return nil
}

// outerClassCaseCollision checks whether outerClass case-insensitively matches
// any name in declNames that is NOT an exact match (exact matches are already
// handled by protoFileToOuterClass appending "OuterClass"). Returns the
// colliding name or "".
func outerClassCaseCollision(outerClass string, declNames []string) string {
	for _, name := range declNames {
		if name != outerClass && strings.EqualFold(name, outerClass) {
			return name
		}
	}
	return ""
}

// protoOuterClassRef returns the fully qualified Java outer class reference for a proto file.
// This is the class that holds the static getDescriptor() method for the file.
func protoOuterClassRef(f *protogen.File) string {
	javaPkg := f.Proto.GetOptions().GetJavaPackage()
	if javaPkg == "" {
		javaPkg = string(f.Proto.GetPackage())
	}

	outerClass := f.Proto.GetOptions().GetJavaOuterClassname()
	if outerClass == "" {
		outerClass = protoFileToOuterClass(f)
	}

	return javaPkg + "." + outerClass
}

// protoFileToOuterClass derives the Java outer class name from a proto file,
// following protoc's algorithm: CamelCase the filename, append "OuterClass" if
// it conflicts with a top-level message, enum, or service name.
func protoFileToOuterClass(f *protogen.File) string {
	base := filepath.Base(f.Proto.GetName())
	base = strings.TrimSuffix(base, filepath.Ext(base))
	name := toUnderscoreCamelCase(base)

	// Check for conflicts with top-level declarations.
	for _, msg := range f.Messages {
		if string(msg.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}
	for _, e := range f.Enums {
		if string(e.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}
	for _, s := range f.Services {
		if string(s.Desc.Name()) == name {
			return name + "OuterClass"
		}
	}

	return name
}

// toUnderscoreCamelCase converts a proto filename base (e.g. "my_file") to CamelCase ("MyFile"),
// matching protoc's java_outer_classname derivation.
func toUnderscoreCamelCase(s string) string {
	var result strings.Builder
	upper := true
	for _, c := range s {
		if c == '_' || c == '-' {
			upper = true
			continue
		}
		if upper {
			result.WriteRune(rune(strings.ToUpper(string(c))[0]))
			upper = false
		} else {
			result.WriteRune(c)
		}
	}
	return result.String()
}

