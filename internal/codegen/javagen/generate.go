func javaTypeInfo(kind protoreflect.Kind, isList bool) (javaType, javaBoxedType, classLiteral string) {
	if isList {
		// For list types, javaBoxedType is the element type (used with ListFlag<E>).
		switch kind {
		case protoreflect.BoolKind:
			return "java.util.List<Boolean>", "Boolean", "Boolean.class"
		case protoreflect.StringKind:
			return "java.util.List<String>", "String", "String.class"
		case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
			return "java.util.List<Long>", "Long", "Long.class"
		case protoreflect.DoubleKind:
			return "java.util.List<Double>", "Double", "Double.class"
		default:
			return "java.util.List<Object>", "Object", "Object.class"
		}
	}
	switch kind {
	case protoreflect.BoolKind:
		return "boolean", "Boolean", "Boolean.class"
	case protoreflect.StringKind:
		return "String", "String", "String.class"
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return "long", "Long", "Long.class"
	case protoreflect.DoubleKind:
		return "double", "Double", "Double.class"
	default:
		return "Object", "Object", "Object.class"
	}
}

func formatJavaDouble(f float64) string {
	s := fmt.Sprintf("%v", f)
	if !strings.Contains(s, ".") {
		s += ".0"
	}
	return s
}

func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i, p := range parts {
		if len(p) > 0 {
			parts[i] = strings.ToUpper(p[:1]) + p[1:]
		}
	}
	return strings.Join(parts, "")
}

func toCamelCase(s string) string {
	pascal := toPascalCase(s)
	if len(pascal) == 0 {
		return pascal
	}
	return strings.ToLower(pascal[:1]) + pascal[1:]
}

func toScreamingSnake(s string) string {
	return strings.ToUpper(s)
}

func quote(s string) string {
	return fmt.Sprintf("%q", s)
}
